    private void InGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_InGuardarActionPerformed
        try {
            int estado = 0;
            capturar();
            int res = buscaProductoEnModelo(cedula, (DefaultTableModel) jTablePersonas.getModel());
            if (res == 1) {
                if (Integer.parseInt(cedula) > 0 && celular > 0 && telefono_fijo > 0 && salario_base > 0 && HED > 0 && HEN > 0) {
                    if (HED <= 60 && HEN <= 60) {
                        if ((departamento == "Gerencia" && salario_base > 3000000)
                                || (departamento == "Administración" && (salario_base >= 2000000 && salario_base <= 3000000))
                                || (departamento == "Planta" && (salario_base >= 200000 && salario_base <= 1000000))) {
                            crear();
                        } else {
                            JOptionPane.showMessageDialog(null, "No coincide el salario con el departamento", "ERROR", 0);
                        }
                    } else {
                        JOptionPane.showMessageDialog(null, "Las horas extras no pueden ser mayor a 60 horas", "SALIDA", JOptionPane.INFORMATION_MESSAGE);
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "No pueden ser numeros negativos", "SALIDA", JOptionPane.INFORMATION_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Ya existe la cedula", "SALIDA", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "VERIFICAR LOS CAMPOS", "SALIDA", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_InGuardarActionPerformed

    private int buscaProductoEnModelo(String cedula, DefaultTableModel modelo) {
        if (cedula != null && modelo != null) {
            for (int renglon = 0; renglon < modelo.getRowCount(); renglon++) {
                String cedulaEnRenglon = (String) modelo.getValueAt(renglon, 0);
                if (cedula.equals(cedulaEnRenglon)) {
                    return -1;
                }
            }
        }
        return 1;
    }


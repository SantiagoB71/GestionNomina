    public void capturar() {
        nombres = InNombres.getText();
        apellidos = InApellidos.getText();
        cedula = InCedula.getText();
        direccion = InDireccion.getText();
        email = InEmail.getText();
        celular = Integer.parseInt(InCelular.getText());
        telefono_fijo = Integer.parseInt(InTelFijo.getText());
        salario_base = Integer.parseInt(InSalBase.getText());
        HED = Integer.parseInt(InHED.getText());
        HEN = Integer.parseInt(InHEN.getText());
        genero = (String) InGenero.getSelectedItem();
        departamento = (String) InDepto.getSelectedItem();
    }

    public void calcular() {
        capturar();
        empleado = new Empleado(nombres, apellidos, cedula, direccion, email, celular, telefono_fijo, salario_base, HED, HEN, genero, departamento);
        empleado.setSalario_base(salario_base);
        InSubTras.setText(Double.toString(empleado.getSubTras()));
        InValorH.setText(Double.toString(empleado.getvalorH()));
        InValorHED.setText(Double.toString(empleado.getValorHED()));
        InValorHEN.setText(Double.toString(empleado.getValorHEN()));
        InPension.setText(Double.toString(empleado.getPension()));
        InSalud.setText(Double.toString(empleado.getSalud()));
        InTotalApo.setText(Double.toString(empleado.getAportes()));
        InSalFinal.setText(Double.toString(empleado.getSalario_final()));
    }

